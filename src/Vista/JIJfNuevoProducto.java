    private void tblProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductosMouseClicked
        int fila = tblProductos.getSelectedRow();
        if (fila == -1) {

            JOptionPane.showMessageDialog(null, "Seleccionar fila por favor");
        } else {
            idc = Integer.parseInt((String) tblProductos.getValueAt(fila, 0).toString());
            String nom = (String) tblProductos.getValueAt(fila, 1);
            double punit = Double.parseDouble((String) tblProductos.getValueAt(fila, 2).toString());
            String tprod = (String) tblProductos.getValueAt(fila, 3);
            String proveedor = (String) tblProductos.getValueAt(fila, 4);
            int stock = Integer.parseInt((String) tblProductos.getValueAt(fila, 5).toString());

            txtID.setText("" + idc);
            txtNombre.setText(nom);
            txtPrecioUnidad.setText("" + punit);
            txtTipo.setText(tprod);
            txtProveedor.setText(proveedor);
            txtStock.setText("" + stock);
        }
    }//GEN-LAST:event_tblProductosMouseClicked

