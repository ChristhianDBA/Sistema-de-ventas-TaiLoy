    void consultar() {
        String sql = "select * from productos";
        try {
            conet = cn1.getConnection();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            Object[] productos = new Object[6];
            modelo = (DefaultTableModel) tblProductos.getModel();
            while (rs.next()) {
                productos[0] = rs.getInt("id");
                productos[1] = rs.getString("nombre");
                productos[2] = rs.getDouble("precio_unitario");
                productos[3] = rs.getString("tipo_de_producto");
                productos[4] = rs.getString("proveedor");
                productos[5] = rs.getInt("stock");
                modelo.addRow(productos);
            }
            tblProductos.setModel(modelo);
        } catch (Exception e) {
            System.out.println("ERROR TABLA DE PRODUCTOS " + e);
        }

    }

    void Agregar() {
        String nom = txtNombre.getText();
        String precio = txtPrecioUnidad.getText();
        String tipo = txtTipo.getText();
        String proveedor = txtProveedor.getText();
        String stock = txtStock.getText();

        try {
            if (txtNombre.getText().isEmpty() || txtTipo.getText().isEmpty() || txtPrecioUnidad.getText().isEmpty() || txtProveedor.getText().isEmpty() || txtStock.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Completar los datos por favor");
                limpiarTabla();
            } else {
                String sql = "insert into productos (nombre, precio_unitario, tipo_de_producto, proveedor, stock) values ('" + nom + "','" + precio + "','" + tipo + "','" + proveedor + "','" + stock + "')";
                conet = cn1.getConnection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "PRODUCTO REGISTRADO");
                limpiarTabla();
                txtID.setText("");
                txtNombre.setText("");
                txtTipo.setText("");
                txtPrecioUnidad.setText("");
                txtProveedor.setText("");
                txtStock.setText("");
            }

        } catch (Exception e) {
            System.out.println("ERROR EN AGREGAR PRODUCTOS " + e);
        }

    }

    void limpiarTabla() {
        while (tblProductos.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }

    void modificar() {
        String nom = txtNombre.getText();
        String precio = txtPrecioUnidad.getText();
        String tipo = txtTipo.getText();
        String proveedor = txtProveedor.getText();
        String stock = txtStock.getText();

        try {
            if (txtNombre.getText().isEmpty() || txtTipo.getText().isEmpty() || txtPrecioUnidad.getText().isEmpty() || txtProveedor.getText().isEmpty() || txtStock.getText().isEmpty() && tblProductos.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(null, "Verificar que la fila este seleccionada, por favor");
                limpiarTabla();
            } else {
                String sql = "update productos set id='" + idc + "', nombre ='" + nom + "', precio_unitario='" + precio + "',tipo_de_producto='" + tipo + "',proveedor = '" + proveedor + "', stock = '" + stock + "' where id = " + idc;
                conet = cn1.getConnection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "PRODUCTO ACTUALIZADO");
                limpiarTabla();
                txtID.setText("");
                txtNombre.setText("");
                txtTipo.setText("");
                txtPrecioUnidad.setText("");
                txtProveedor.setText("");
                txtStock.setText("");
            }

        } catch (Exception e) {
            System.out.println("ERROR EN ACTUALIZAR PRODUCTOS " + e);
        }
    }

    void eliminar() {
        int fila = tblProductos.getSelectedRow();
        try {
            if (fila < 0) {
                JOptionPane.showMessageDialog(null, "Seleccionar un producto de la fila, por favor");
                limpiarTabla();
            } else {
                int confirmacion = JOptionPane.showConfirmDialog(null, "¿Estás seguro de eliminar esta fila?", "Confirmación", JOptionPane.YES_NO_OPTION);

                if (confirmacion == JOptionPane.YES_OPTION) {
                    String sql = "delete from productos where id = " + idc;
                    conet = cn1.getConnection();
                    st = conet.createStatement();
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "PRODUCTO ELIMINADO");
                    limpiarTabla();
                } else {
                    limpiarTabla();
                }
            }
        } catch (Exception e) {
            System.out.println("ERROR AL ELIMINAR UN PRODUCTO " + e);
        }
    }

